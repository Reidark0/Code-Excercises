# Fatiando listas
# 
# Crie uma lista com 6 elementos e utilize o fatiamento 
# para imprimir na tela as seguintes sub-listas: 
# 1 - Os três primeiros elementos
# 2 - Os três últimos elementos
# 3 - Sub-lista com o segundo e o terceiro elemento.

lista = [1, 2, 3, 4, 5, 6]
print(lista[:3])
print(lista[3:])
print(lista[1:3])